def sort_step(to_sort):
    length = len(to_sort)

    if length == 1:
        return to_sort

    if length == 2:
        if to_sort[0] > to_sort[1]:
            to_sort.reverse()
        return to_sort

    middle = length // 2
    left = sort_step(to_sort[:middle])
    right = sort_step(to_sort[middle:])

    _sorted_list = []
    right_count = 0
    left_count = 0
    while right_count < len(right):
        if (left_count >= len(left) or right[right_count] <= left[left_count]):
            _sorted_list.append(right[right_count])
            right_count += 1
        else:
            _sorted_list.append(left[left_count])
            left_count += 1
    while left_count < len(left):
        _sorted_list.append(left[left_count])
        left_count += 1
    
    return _sorted_list


